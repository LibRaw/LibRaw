const uint8_t* lookup_gtli_table(int Bp, int Br) {
    for (int i = 0; i < kNumGtliRows; ++i) {
        if (kGtliTable[i].Bp == Bp && kGtliTable[i].Br == Br) {
            return kGtliTable[i].values;
        }
    }
    return nullptr;
}

